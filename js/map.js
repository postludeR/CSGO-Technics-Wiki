



function infer() {

    let myTimer = -1;

    let scrollTo = 0;

    if (myTimer === -1) {
        myTimer = setInterval(() => {

            scrollTo += 50;

            if (scrollTo >= 1520) {
                scrollTo = 1520;
                window.clearInterval(myTimer);
                myTimer = -1;
            }

            window.scrollTo(0, scrollTo)
        }, 7);

    }

}


